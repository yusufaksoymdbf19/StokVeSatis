package ex2_1_1;

public class IncDec {
	public static void main(String[] argStrings) {
		int x = 8, y = 13;
		System.out.println("x = "+x);
		System.out.println("y = " + y);
		System.out.println("++x = " + ++x);
		System.out.println("y++ = " + y++);
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		
	}
}
